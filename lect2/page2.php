PHP code may be executed with a command line interface (CLI), embedded into HTML code,
 or used in combination with various web template systems, web content management systems, 
 and web frameworks. PHP code is usually processed by a PHP interpreter implemented 
 as a module in a web server or as a Common Gateway Interface (CGI) executable. 
 The web server outputs the results of the interpreted and executed PHP code, 
 which may be any type of data, such as generated HTML code or binary image data.
